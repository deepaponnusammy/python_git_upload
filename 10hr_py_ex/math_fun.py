# Math Functions in Python
# The math module is used to access mathematical functions in the
# Python. All methods of this functions are used
# for integer or real type objects, not for complex numbers.
import math

print(math.sqrt(4))
print(math.ceil(1.55555))
print(math.floor(1.55555))
# 5! => 1*2*3*4*5
print(math.factorial(5))
print(math.fabs(-5))
print(math.pow(2, 3))
print(math.log2(2))
print(math.log10(2))
print(math.pi)
print(math.e)